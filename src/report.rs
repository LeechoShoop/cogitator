#[cfg(test)]
mod tests {
    use super::*;

    fn finding(sev: Severity, check_name: &str) -> ScanFinding {
        ScanFinding {
            check_name: check_name.to_string(),
            severity: sev,
            evidence: "root:x:0:0".to_string(),
            request_raw: "GET /etc/passwd HTTP/1.1".to_string(),
            response_snippet: "root:x:0:0:root:/root:/bin/bash".to_string(),
            url: "http://example.com/download?file=a".to_string(),
            parameter: Some("file".to_string()),
        }
    }

    #[test]
    fn escapes_script_tags_in_evidence() {
        let mut f = finding(Severity::Critical, "XSS");
        f.evidence = "<script>alert(1)</script>".to_string();
        let html = generate_html(&[f], &ReportMeta {
            domain: "example.com".to_string(),
            timestamp_ms: 1_700_000_000_000,
        });
        assert!(!html.contains("<script>alert(1)</script>"));
        assert!(html.contains("&lt;script&gt;"));
    }

    #[test]
    fn groups_by_severity_worst_first() {
        let findings = vec![
            finding(Severity::Info, "Info Check"),
            finding(Severity::Critical, "Critical Check"),
            finding(Severity::Medium, "Medium Check"),
        ];
        let html = generate_html(&findings, &ReportMeta {
            domain: "example.com".to_string(),
            timestamp_ms: 1_700_000_000_000,
        });
        let crit_pos = html.find("Critical Check").unwrap();
        let med_pos = html.find("Medium Check").unwrap();
        let info_pos = html.find("Info Check").unwrap();
        assert!(crit_pos < med_pos);
        assert!(med_pos < info_pos);
    }

    #[test]
    fn empty_severity_group_omits_section() {
        let findings = vec![finding(Severity::Critical, "Only Critical")];
        let html = generate_html(&findings, &ReportMeta {
            domain: "example.com".to_string(),
            timestamp_ms: 1_700_000_000_000,
        });
        assert!(!html.contains(r#"class="severity-section high""#));
        assert!(html.contains(r#"class="severity-section critical""#));
    }

    #[test]
    fn includes_all_required_fields() {
        let f = finding(Severity::High, "SQLi");
        let html = generate_html(&[f], &ReportMeta {
            domain: "target.tld".to_string(),
            timestamp_ms: 1_700_000_000_000,
        });
        assert!(html.contains("SQLi"));
        assert!(html.contains("example.com/download"));
        assert!(html.contains("file"));
        assert!(html.contains("root:x:0:0"));
        assert!(html.contains("GET /etc/passwd HTTP/1.1"));
    }

    #[test]
    fn no_external_assets() {
        let html = generate_html(&[], &ReportMeta {
            domain: "example.com".to_string(),
            timestamp_ms: 0,
        });
        assert!(!html.contains("http://") || !html.contains("<link"));
        assert!(!html.contains("<script src"));
        assert!(!html.contains("<link rel=\"stylesheet\""));
    }

    #[test]
    fn timestamp_formats_known_epoch() {
        // 1_700_000_000_000 ms == 2023-11-14 22:13:20 UTC
        assert_eq!(format_timestamp_utc(1_700_000_000_000), "2023-11-14 22:13:20");
    }

    #[test]
    fn timestamp_epoch_zero() {
        assert_eq!(format_timestamp_utc(0), "1970-01-01 00:00:00");
    }
}